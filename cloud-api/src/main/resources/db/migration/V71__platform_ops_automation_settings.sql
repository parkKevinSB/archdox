create table platform_ops_automation_settings (
    singleton_key varchar(64) primary key,
    detection_enabled boolean not null,
    detection_check_interval_ms bigint not null,
    document_job_stuck_minutes bigint not null,
    agent_command_stuck_minutes bigint not null,
    photo_pickup_stuck_minutes bigint not null,
    delivery_stuck_minutes bigint not null,
    max_detected_items integer not null,
    daily_report_enabled boolean not null,
    daily_report_run_time varchar(16) not null,
    daily_report_zone_id varchar(80) not null,
    daily_report_check_interval_ms bigint not null,
    daily_report_catch_up_grace_minutes bigint not null,
    daily_report_auto_diagnosis_enabled boolean not null,
    daily_report_auto_diagnosis_incident_limit integer not null,
    daily_report_auto_diagnosis_min_severity varchar(40) not null,
    daily_report_directory text not null,
    retention_enabled boolean not null,
    retention_days integer not null,
    retention_check_interval_ms bigint not null,
    updated_by_user_id bigint references users(id),
    created_at timestamptz not null,
    updated_at timestamptz not null
);
